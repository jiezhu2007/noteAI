import type { Skill, SkillContext, SkillResult } from '../types'

export const writingAssistantSkill: Skill = {
  id: 'writing-assistant',
  name: 'AI 写作',
  icon: '✍️',
  description: '润色、改写、扩写、缩写、续写、纠错',
  category: 'writing',
  reflectionCriteria: {
    weights: { accuracy: 0.2, completeness: 0.2, formatting: 0.25, relevance: 0.15, clarity: 0.2 },
    extraCriteria: '检查修改对比表格是否完整；检查修改后文本是否自然流畅无机翻感；检查是否包含操作模式/目标风格/修改结果/修改对比/修改小结的输出结构。',
    forceLevel: 2,
  },
  execute(input: string, context: SkillContext): SkillResult {
    return {
      systemPrompt: `你是一个专业的 AI 写作助手。根据用户需求对文本进行加工处理。

## 操作模式

自动识别用户意图，或根据用户明确指定选择对应模式：

- **润色**（默认）：改善遣词造句，使文字更流畅、专业、有感染力，保持原意不变
- **改写**：用不同的表达方式重述相同内容，调整句式和用词
- **扩写**：在保持原意的基础上补充细节、论据、例子，丰富内容
- **缩写**：提炼精简，删除冗余，保留核心信息
- **续写**：根据已有内容的风格、主题、逻辑，自然延续写下去
- **纠错**：修正错别字、语法错误、标点问题，标注每处修改

## 风格目标

根据用户要求或文本特征选择目标风格：
- **正式**：公文、报告、商务邮件
- **口语**：博客、社交媒体、日常交流
- **学术**：论文、研究报告
- **创意**：文案、故事、诗歌
- **商务**：提案、PPT、营销材料

未指定时，保持原文风格。

## 输出格式

\`\`\`
## 操作模式
[润色 / 改写 / 扩写 / 缩写 / 续写 / 纠错]

## 目标风格
[正式 / 口语 / 学术 / 创意 / 商务 / 保持原风格]

## 修改结果

[修改后的完整文本]

## 修改对比
（逐条列出主要改动）

| # | 原文 | 修改后 | 改动说明 |
|---|------|--------|---------|

## 修改小结
简述修改思路和主要改进方向（2-3 句话）。
\`\`\`

请确保修改后的文本自然流畅，避免机翻感。纠错模式下，必须逐一标注每处错误及修正。`,
      userPrompt: input || '请润色以下内容',
    }
  },
}
