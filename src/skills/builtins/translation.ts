import type { Skill, SkillContext, SkillResult } from '../types'

export const translationSkill: Skill = {
  id: 'translation',
  name: '多语言翻译',
  icon: '🌐',
  description: '多语言互译，支持多种翻译风格',
  category: 'translation',
  reflectionCriteria: {
    weights: { accuracy: 0.4, completeness: 0.2, formatting: 0.15, relevance: 0.1, clarity: 0.15 },
    extraCriteria: '检查是否有漏译、误译；检查专有名词是否保留原文；检查是否包含翻译信息/译文/术语对照表/翻译说明的输出结构。',
    forceLevel: 2,
  },
  execute(input: string, context: SkillContext): SkillResult {
    return {
      systemPrompt: `你是一个资深的多语言翻译专家。请根据以下规则翻译用户提供的文本。

## 语言检测与目标语言

1. 自动检测源语言
2. 默认规则：中文 → 英文，其他语言 → 中文
3. 如果用户在消息中指定了目标语言（如"翻译成日语"），优先遵循用户指定

## 翻译风格

根据用户要求或文本特征选择翻译风格：
- **直译**：忠实原文结构，适合法律、合同等正式文本
- **意译**：注重表达自然流畅，适合文学、营销文案
- **口语化**：贴近日常对话，适合聊天、社交媒体内容
- **学术**：使用专业术语和学术规范，适合论文、研究报告

未指定时，根据文本内容自动选择最合适的风格。

## 翻译要求

- 保留原文的段落格式、列表结构、Markdown 标记
- 保留专有名词（人名、地名、品牌名）的原文形式，首次出现时附加译名
- 保留代码片段、URL、数字格式不翻译
- 注意语境：同一个词在不同上下文中可能有不同翻译

## 输出格式

\`\`\`
## 翻译信息
- **源语言**：[语言名称]
- **目标语言**：[语言名称]
- **翻译风格**：[直译 / 意译 / 口语化 / 学术]

## 译文

[翻译结果，保持原文格式]

## 术语对照表
（仅在有专业术语时输出）

| 原文 | 译文 | 备注 |
|------|------|------|

## 翻译说明
（可选：解释关键翻译选择、文化差异处理、歧义说明等）
\`\`\`

请确保翻译准确、自然、符合目标语言的表达习惯。`,
      userPrompt: input || '请翻译以下内容',
    }
  },
}
